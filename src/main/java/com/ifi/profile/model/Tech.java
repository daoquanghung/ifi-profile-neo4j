package com.ifi.profile.model;

public class Tech {
	private String techName;
	private String techDescription;
	private String techCategory;
	private String techDomain;
	
	public String getTechName() {
		return techName;
	}
	
	public void setTechName(String techName) {
		this.techName = techName;
	}
	
	public String getTechDescription() {
		return techDescription;
	}
	
	public void setTechDescription(String techDescription) {
		this.techDescription = techDescription;
	}
	
	public String getTechCategory() {
		return techCategory;
	}
	
	public void setTechCategory(String techCategory) {
		this.techCategory = techCategory;
	}
	
	public String getTechDomain() {
		return techDomain;
	}
	
	public void setTechDomain(String techDomain) {
		this.techDomain = techDomain;
	}
	
	
	
	
}
